  setCursor(&lcd, 0, 1);
  print(&lcd, "SmartFeeder!");

  int quantity = 130;

  feederInit(&feeder, quantity, FEEDER_ON_GPIO_Port, FEEDER_ON_Pin, FEEDER_EMPTY_GPIO_Port, FEEDER_EMPTY_Pin);
